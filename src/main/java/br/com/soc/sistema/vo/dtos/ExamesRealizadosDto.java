package br.com.soc.sistema.vo.dtos;

public class ExamesRealizadosDto {
	private String cod;
	private String codExame;
	private String exame;
	private String codFunc;
	private String func;
	private String data;
	
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getExame() {
		return exame;
	}
	public void setExame(String exame) {
		this.exame = exame;
	}
	public String getFunc() {
		return func;
	}
	public void setFunc(String func) {
		this.func = func;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getCodExame() {
		return codExame;
	}
	public void setCodExame(String codExame) {
		this.codExame = codExame;
	}
	public String getCodFunc() {
		return codFunc;
	}
	public void setCodFunc(String codFunc) {
		this.codFunc = codFunc;
	}
	
	
}
